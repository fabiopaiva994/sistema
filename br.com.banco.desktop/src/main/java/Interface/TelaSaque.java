    public void digita(int num) {
        if (varAgencia.isEnabled() == true) {
            varAgencia.setText(varAgencia.getText() + num);
        } else if (varConta.isEnabled() == true) {
            varConta.setText(varConta.getText() + num);
        } else if (varSenha.isEnabled() == true) {
            varSenha.setText(varSenha.getText() + num);
        }
    }

    public void voltar() {
        if (varAgencia.isEnabled() == false && varConta.isEnabled() == true) {
            varConta.setEnabled(false);
            varAgencia.setEnabled(true);
            varAgencia.requestFocus();
        } else if (varAgencia.isEnabled() == false && varConta.isEnabled() == false
                && varSenha.isEnabled() == true) {
            varSenha.setEnabled(false);
            varConta.setEnabled(true);
            varConta.requestFocus();
        } else if (varSenha.isEnabled() == false) {
            varSenha.setEnabled(true);
            foco();
        }
    }

    public void corrige() {
        if (varAgencia.isEnabled() == true) {
            varAgencia.setText("");
            varAgencia.requestFocus();
        } else if (varConta.isEnabled() == true) {
            varConta.setText("");
            varConta.requestFocus();
        } else if (varSenha.isEnabled() == true) {
            varSenha.setText("");
            varSenha.requestFocus();
        }
    }

    public void foco() {
        if (varAgencia.isEnabled() == true) {
            varAgencia.requestFocus();
        } else if (varConta.isEnabled() == true) {
            varConta.requestFocus();
        } else if (varSenha.isEnabled() == true) {
            varSenha.requestFocus();
        }
    }


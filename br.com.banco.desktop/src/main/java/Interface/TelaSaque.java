    private void btnVoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoltarActionPerformed
        if(varAgencia.isEnabled() == false && varConta.isEnabled() == true && varSenha.isEnabled() == false) {
            varAgencia.setEnabled(true);
            varConta.setEnabled(false);
            
        } else if(varConta.isEnabled() == false && varAgencia.isEnabled() == false && varSenha.isEnabled() == true) {
            varAgencia.setEnabled(false);
            varConta.setEnabled(true);
            varSenha.setEnabled(false);
        }else if(varConta.isEnabled() == false && varAgencia.isEnabled() == false && varSenha.isEnabled() == false){
            varSenha.setEnabled(true);
        }
    }//GEN-LAST:event_btnVoltarActionPerformed

